void	ft_striteri(char *s, void (*f)(unsigned int, char*))
{
	unsigned int	i;

	if (!f || !s)
		return ;
	i = 0;
	while (((char *)s)[i] != '\0')
	{
		f(i, &((char *)s)[i]);
		i++;
	}
}
