int	ft_atoi(const char *str)
{
	int	i;
	int	j;
	int	nb;

	nb = 0;
	j = 1;
	i = 0;
	while (str[i] == '\f' || str[i] == '\n' || str[i] == '\r'
		|| str[i] == '\t' || str[i] == '\v' || str[i] == ' ')
		i++;
	if (((char *)str)[i] == '-' && ((char *)str)[i + 1] != '-')
	{
		j = -1;
		i++;
	}
	else if (((char *)str)[i] == '+')
		i++;
	while (((char *)str)[i] >= '0' && ((char *)str)[i] <= '9')
	{
		nb *= 10;
		nb += (((char *)str)[i] - 48);
		i++;
	}
	return (nb * j);
}
